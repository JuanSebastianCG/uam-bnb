    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = Auth::user();
        $properties = $user->property()
        ->orderBy('created_at', 'desc')->get();

        return view('properties.index', compact('properties', 'user'));
    }

